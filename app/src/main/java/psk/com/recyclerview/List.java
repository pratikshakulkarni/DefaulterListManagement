public class List {

    private String name,rno,per;


    public List(String name, String rno,String per) {
        this.name = name;
        this.rno = rno;
        this.per = per;
    }

    public String getName() {
        return name;
    }

    public String getPer() {
        return name;
    }


    public String getRno() {
        return rno;
    }
}
